import heapq

# 1. Setup Goal and Moves
GOAL = "123804765"
# Which indexes can '0' swap with
MOVES = {
    0: (1, 3),    1: (0, 2, 4),    2: (1, 5),
    3: (0, 4, 6), 4: (1, 3, 5, 7), 5: (2, 4, 8),
    6: (3, 7),    7: (4, 6, 8),    8: (5, 7)
}

def get_heuristic(state):
    return sum(1 for i in range(9) if state[i] != '0' and state[i] != GOAL[i])

def a_star(start):
    # Priority Queue: (f-score, g-score, current_state, path_history)
    pq = [(get_heuristic(start), 0, start, "")]
    visited = set()

    while pq:
        f, g, current, path = heapq.heappop(pq)

        if current == GOAL:
            return path + current, g

        if current in visited:
            continue
        visited.add(current)

        # Find '0' and generate moves
        zero_idx = current.index("0")
        for move_idx in MOVES[zero_idx]:
            # Swap 0 to create new state
            new_list = list(current)
            new_list[zero_idx], new_list[move_idx] = new_list[move_idx], new_list[zero_idx]
            new_state = "".join(new_list)

            if new_state not in visited:
                new_g = g + 1
                new_f = new_g + get_heuristic(new_state)
                heapq.heappush(pq, (new_f, new_g, new_state, path + current + "\n⬇\n"))

    return "No Solution", 0

start_state = input("Enter start state (e.g., 283164705): ")
