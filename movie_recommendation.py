def fitness(x):
    return abs(a*x*x + b*x + c)

def select(pop):
    p1, p2 = random.sample(pop, 2)
    return p1 if fitness(p1) < fitness(p2) else p2

def genetic():
    pop_size = 50
    pop = [random.uniform(-50, 50) for _ in range(pop_size)]
    for _ in range(200):
        new = []
        for _ in range(pop_size):
            p1 = select(pop)
            p2 = select(pop)
            child = (p1 + p2) / 2
            child += random.uniform(-0.5, 0.5)
            new.append(child)
        pop = new
    return min(pop, key=lambda x: fitness(x))

